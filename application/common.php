/**
 * 通用化API接口数据输出
 * @param int $status
 * @param string $message
 * @param [] $data
 * @param int $httpCode
 * @return json
 */
function apiReturn($status, $message, $data=[], $httpCode=200)
{
    $result = [
        'status'  => $status,
        'message' => $message,
        'data'    => $data,
    ];

    return json($result, $httpCode);
}